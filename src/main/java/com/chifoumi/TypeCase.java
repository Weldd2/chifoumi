package com.chifoumi;
/**
 * Indique le type d'une case du plateau.
 */
public enum TypeCase {
    /**
     * Indique que la case est un mur.
     */
    MUR,
    /**
     * Indique que la case est un pion (ou une pile).
     */
    PION,
    /**
     * Indique que la case est vide.
     */
    VIDE,
}